/*

Write a program that will iterate from 0 to 15. For each iteration, 
it will check if the current number is odd or even, and display a message to the screen.

console output:

0 is even
1 is odd
2 is even
3 is odd
4 is even
.
.
.

Hint: Read about the Modulo(Remainder)-Operator here to get to know if something is odd or even:
https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Operators/Remainder

*/

for (let i = 0; i <= 15; i++) {
  if (i % 2 === 0) {
    console.log(`${i} is even`);
  } else {
    console.log(`${i} is odd`);
  }
}
