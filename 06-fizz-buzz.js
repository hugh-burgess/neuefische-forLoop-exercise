/*
 
 Write a JavaScript program which iterates the integers from 1 to 100.
 But for multiples of three print "Fizz" instead of the number and for the multiples of five print "Buzz". 
 For numbers which are multiples of both three and five print "FizzBuzz". If none of the above applied
 print the number.

 console output:

 0
 1
 2
 Fizz
 4
 Buzz
 5
 Fizz
 7
 8
 Fizz
 Buzz
 11
 Fizz
 13
 14
 FizzBuzz
 .
 .
 .
 
 Hint: Read about the Modulo(Remainder)-Operator here to get to know if something is odd or even:
https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Operators/Remainder

 */

for (let i = 0; i <= 100; i++) {
  if (i % 3 === 0 && i % 5 === 0) {
    console.log("FizzBuzz");
  } else if (i % 5 === 0) {
    console.log("Buzz");
  } else if (i % 3 === 0) {
    console.log("Fizz");
  } else {
    console.log(i);
  }
}
